    public static float obtenerMontoTotal(String user) {
        float montoTotal = 0;
        try {
            JSONObject jsonObj = new JSONObject(getJSONString(".\\src\\forms\\dataSrc/" + user + ".json"));
            JSONArray result = jsonObj.getJSONArray("gastos");
            for (int i = 0; i < result.length(); i++) {
                JSONObject jsonObject = result.getJSONObject(i);
                montoTotal += (float) jsonObject.getFloat("monto");
            }
            return montoTotal;
        } catch (Exception e) {
            System.out.println(e);
            return -1;
        }
    }

    public void agregarSaldo(String saldoPAgregar, String user) {
        try {
            float saldoAgregar = Float.parseFloat(saldoPAgregar);
            String jsonData = getJSONString(".\\src\\forms\\dataSrc/" + user + ".json");
            ArrayList gastos = getArrayFromJson(jsonData);
            Usuario usuario = getObj(user, gastos);
            float saldoAntiguo = usuario.getSaldo();
            usuario.setSaldo(saldoAntiguo + saldoAgregar);
            writeJson(usuario, gastos);
            lblNuevoSaldo.setText("El nuevo saldo es $" + (usuario.getSaldo() - obtenerMontoTotal(user)));

        } catch (Exception e) {
            System.out.println(e);
            lblNuevoSaldo.setText("No se ha podido actualizar el saldo");
        }
    }

    public static void writeJson(Usuario user, ArrayList gastos) {
        String path = ".\\src\\forms\\dataSrc/" + user.getNombre() + ".json";
        JSONObject jsonObj = new JSONObject();
        try {
            jsonObj.put("usuario", user.getNombre());
            jsonObj.put("edad", user.getEdad());
            jsonObj.put("celular", user.getCelular());
            jsonObj.put("saldo", user.getSaldo());
            jsonObj.put("gastos", gastos);
            jsonObj.put("contrasenia", user.getContrasenia());
        } catch (JSONException e) {
            e.printStackTrace();
        }

        try ( PrintWriter out = new PrintWriter(new FileWriter(path))) {
            out.write(jsonObj.toString());

        } catch (Exception e) {
            System.out.println(e);
        }

    }
